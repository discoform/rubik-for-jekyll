// <!-- Main Navigation Toggle -->
function mainNavigation() { var x = document.getElementById("main-menu"); if (x.className === "topnav") { x.className += " responsive"; } else { x.className = "topnav"; } }
// <!-- Mobile Footer Toggle -->
function mobileFooter() { var x = document.getElementById("main-footer"); if (x.className === "site-footer") { x.className += " responsive"; } else { x.className = "site-footer"; } }
